package com.pennanttech.Empmodifications;

public class Stockbean {
	private int userid;
	private String vehicleid;
	private String vehiclename;
	private String vehiclemodel;
	private int manfactureyear;
	private String vehiclecolour;
	private int vehiclecost;
	private String vehicletype;
	private int stock;

	public int getUserid() {
		return userid;
	}

	public void setUserid(int userid) {
		this.userid = userid;
	}

	public String getVehicleid() {
		return vehicleid;
	}

	public void setVehicleid(String vehicleid) {
		this.vehicleid = vehicleid;
	}

	public String getVehiclename() {
		return vehiclename;
	}

	public void setVehiclename(String vehiclename) {
		this.vehiclename = vehiclename;
	}

	public String getVehiclemodel() {
		return vehiclemodel;
	}

	public void setVehiclemodel(String vehiclemodel) {
		this.vehiclemodel = vehiclemodel;
	}

	public int getManfactureyear() {
		return manfactureyear;
	}

	public void setManfactureyear(int manfactureyear) {
		this.manfactureyear = manfactureyear;
	}

	public String getVehiclecolour() {
		return vehiclecolour;
	}

	public void setVehiclecolour(String vehiclecolour) {
		this.vehiclecolour = vehiclecolour;
	}

	public int getVehiclecost() {
		return vehiclecost;
	}

	public void setVehiclecost(int vehiclecost) {
		this.vehiclecost = vehiclecost;
	}

	public String getVehicletype() {
		return vehicletype;
	}

	public void setVehicletype(String vehicletype) {
		this.vehicletype = vehicletype;
	}

	public int getStock() {
		return stock;
	}

	public void setStock(int stock) {
		this.stock = stock;
	}

}
